    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $customerId = $id;
        $customerName = DB::table('Customer')->where('id', $customerId)->pluck('name');

        $lists = DB::table('kerjasama')->where('customer_id', $customerId)->get();

        $vendorName = [];
        for ($i=0; $i < count($lists); $i++) { 
            $nameVendor = DB::table('Vendor')->where('id', $lists[$i]->vendor_id)->pluck('name');
            $vendorName[$i] = $nameVendor;
        }

        return view::make('tables',compact('customerName', 'vendorName'));
    }

